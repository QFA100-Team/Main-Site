     */
    public function show($id)
    {
        //return News::with('author')->with('author_roles')->get();
        $news = News::find($id);
        if (is_null($news)) return redirect('/news')->with('error', 'News article could not be found.');

        return view('news.news-show')->with('news', $news);
    }

